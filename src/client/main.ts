// Smooth scrolling for navigation links
document.querySelectorAll<HTMLAnchorElement>('a[href^="#"]').forEach((anchor: HTMLAnchorElement): void => {
    anchor.addEventListener('click', (e: Event): void => {
        e.preventDefault();
        const href: string | null = anchor.getAttribute('href');
        if (href) {
            const target: Element | null = document.querySelector(href);
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        }
    });
});

// Intersection Observer for fade-in animations
interface ObserverOptions {
    root: null;
    rootMargin: string;
    threshold: number;
}

const observerOptions: ObserverOptions = {
    root: null,
    rootMargin: '0px',
    threshold: 0.1
};

const observer: IntersectionObserver = new IntersectionObserver((entries: IntersectionObserverEntry[]): void => {
    entries.forEach((entry: IntersectionObserverEntry): void => {
        if (entry.isIntersecting) {
            entry.target.classList.add('fade-in');
            observer.unobserve(entry.target);
        }
    });
}, observerOptions);

// Observe all sections
document.querySelectorAll<HTMLElement>('section').forEach((section: HTMLElement): void => {
    section.style.opacity = '0';
    section.style.transform = 'translateY(20px)';
    section.style.transition = 'opacity 0.6s ease-out, transform 0.6s ease-out';
    observer.observe(section);
});

